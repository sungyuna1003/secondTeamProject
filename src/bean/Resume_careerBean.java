package bean;

public class Resume_careerBean {
	
	private int rcid;
	private int rid;
	private String r_company;
	private String entered_date;
	private String resignation_date;
	private String job_title;
	
	public int getRcid() {
		return rcid;
	}
	public void setRcid(int rcid) {
		this.rcid = rcid;
	}
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getR_company() {
		return r_company;
	}
	public void setR_company(String r_company) {
		this.r_company = r_company;
	}
	public String getEntered_date() {
		return entered_date;
	}
	public void setEntered_date(String entered_date) {
		this.entered_date = entered_date;
	}
	public String getResignation_date() {
		return resignation_date;
	}
	public void setResignation_date(String resignation_date) {
		this.resignation_date = resignation_date;
	}
	public String getJob_title() {
		return job_title;
	}
	public void setJob_title(String job_title) {
		this.job_title = job_title;
	}

}
